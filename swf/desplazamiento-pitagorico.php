			<span class="sub-blue twriter" >Desplazamiento Pitagórico</span>
			<p>
			Instalación, 24 piezas de espejos<br />
            Conectores de aluminio<br/>
             Medidas variables<br />
<br />
Trabajé alrededor de la idea de fragilidad y tensión, utilizando 24 piezas de espejos encontrados durante la remodelación una vieja casa de los cincuenta en la colonia Narvarte. Los espejos de formas triangulares y romboides muestran en ellos las huellas del tiempo (fisuras, manchas, desgastes) que hacen de la figura geométrica pura, un objeto imperfecto. El espejo viejo refleja una especie de fantasma; su nebulosidad recuerda casi dolorosamente la nitidez que un día tuvo.<br />
Las piezas planas serán levantadas en figuras tridimensionales a manera de ejercicio de papiroflexia, realizando in situ figuras geométricas con los materiales a la mano.<br /><br /><br /><br />
